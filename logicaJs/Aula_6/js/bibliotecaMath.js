function round() { // 78.9876
    var num = 78.9876;
    var resultado = Math.round(num);

    alert(resultado);
}

function random() {
    var resultado = Math.random();

    alert(resultado);
}

function min() {
    var resultado = Math.min(1, 5, 10);

    alert(resultado);
}

function max() {
    var resultado = Math.max(1, 5, 10);

    alert(resultado);
}

function trunc() {
    var resultado = Math.trunc(78.9876);

    alert(resultado);
}